public class Unboxing {
    public static void main(String args[]) {
        
        Integer i1 = 120; 
        int i2 = i1; 
        
        System.out.println(i2);
        Boolean b1 = false;
        boolean b2 = b1;
        System.out.println(b2);
        Float f1 = 31.00f;
        float f2 = f1;
        
        System.out.println(f2);
        Byte a1 = 122;
        byte a2 = a1;

        System.out.println(a2);



    }
}
