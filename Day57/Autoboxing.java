class Autoboxing {

    public static void main(String args[]) {
    
        int n1 = 120; 

        Integer n2 = n1; 

        System.out.println(n2);
        
        byte b1 = 120;
        
        Byte b2 = b1;
        
        System.out.println(b2);
        
        float f1 = 30.21f;
        
        Float f2 = f1;
        
        System.out.println(f2);
        
        
    }
}
