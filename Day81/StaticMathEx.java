import static java.lang.Math.*;

public class StaticMathEx {
    public static void main(String[] args) {

        System.out.println(ceil(2.5));

        System.out.println(round(3.15));

        System.out.println(floor(3.6));

        System.out.println(sqrt(55));

        System.out.println(pow(8,2));

        System.out.println(abs(-30));

        System.out.println(min(10,40));

        System.out.println(max(21,15));
    }
}
